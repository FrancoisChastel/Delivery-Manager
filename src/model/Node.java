package model;


public class Node {
	
	private int idNode;
	private int x;
	private int y;
	
	
	
	public Node(int idNode, int x, int y) {
		this.idNode = idNode;
		this.x = x;
		this.y = y;
	}
	
	// Getters and Setters
	
	public int getidNode() {
		return idNode;
	}



	public void setidNode(int idNode) {
		this.idNode = idNode;
	}



	public int getX() {
		return x;
	}



	public void setX(int x) {
		this.x = x;
	}



	public int getY() {
		return y;
	}



	public void setY(int y) {
		this.y = y;
	}
	
	

	
	
	
}
